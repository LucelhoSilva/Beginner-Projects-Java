    private void jButtonIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIgualActionPerformed
        /*
        Aqui utlizaremos de uma biblioteca para converter a expressão que montamos em texto para formato de dado numérico.
        Só assim será possível efetuar a operação matemática.
        Bibliotecas são códigos externos ou internos já prontos. Normalmente executam funções já muito conhecidas/utilizadas
        ou que deem trabalho e sejam repetitivas.
        */
        Expression expression = new ExpressionBuilder(expressaoMatematica).build();
        double resultado = expression.evaluate();

        /*
        Mostraremoso o resultado final na tela
        */
        jTextFieldVisorResultado.setText(String.valueOf(resultado));

        /*
        salvaremos esse resultado no histórico utilizando o método previamente criado.
        */
        historico.salvar(expressaoVisual, String.valueOf(resultado));
    }//GEN-LAST:event_jButtonIgualActionPerformed

