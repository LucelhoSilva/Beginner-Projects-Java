    private void jButtonMaisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMaisActionPerformed
        /*
        A lógica a seguir, e para todos os botões de operação ou número, é:
        um if verifica se o campo de texto para resultado está vazio. Se elenão estiver vazia, o limparemos.
        */
        if(!jTextFieldVisorResultado.equals("")){
            jTextFieldVisorResultado.setText("");
        }
        /*
        Em seguida, adiconaremos o símbolo de adição para a expressão que o usuário está formando.
        Há uma variável para expressão "matemática" e outra para "visual" pois os símbolos que estamos acostumados
        como "÷" para o botão de divisão ou "x' para o de multiplicação, não são reconhecidos pelo algoritmo como
        operadores aritméticos.
        No caso, a divisão seria barra (/) e a multiplicação seria asterísco (*).
        Então vamos criar uma expressão para realmente calcularmos e outra para mostrarmos de forma bonita/legível/familiar ao usuário.
        */
        expressaoMatematica += "+";
        expressaoVisual += "+";
        /*
        O comando a seguir pega o campo de texto destinado à aprsentação visual da conta e define seu texto como o valor contido na
        variável "expressaoVisual".
        */
        jTextFieldVisorExpressao.setText(expressaoVisual);
    }//GEN-LAST:event_jButtonMaisActionPerformed

