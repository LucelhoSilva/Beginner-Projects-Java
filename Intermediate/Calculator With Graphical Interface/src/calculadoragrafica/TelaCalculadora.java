    private void jButtonApagarCaractereActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonApagarCaractereActionPerformed
        /*
        Se vamos apagar um caractere da conta, quer dizer que não terminamos.
        Assim, se o campo resultado não estiver vazio, o limparemos para pode continuar a conta e não confundir o usuário
        com um valor previamente obtido.
        */
        if (!"".equals(jTextFieldVisorResultado.getText())) {
            jTextFieldVisorResultado.setText("");
        }

        /*
        utilizares o método previamente criado na classeMain e definiremos o texto do campo de visão como o texto que o método retornar
        já sem o ultimo caractere.
        */
        jTextFieldVisorExpressao.setText(classeMain.removerCaractere(jTextFieldVisorExpressao.getText()));
        /*
        não devemos remover o último carectere apenas visualmente, mas também das variáveis para que a conta não dê errado e distoe do que
        estamos vizualizando.
        */
        expressaoMatematica = classeMain.removerCaractere(expressaoMatematica);
        expressaoVisual = classeMain.removerCaractere(expressaoVisual);
    }//GEN-LAST:event_jButtonApagarCaractereActionPerformed

    private void jButtonHistoricoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHistoricoActionPerformed
        
        /*
        Quando o usuário quiser vizualizar o histórico, abriremos uma nova tela para uma melhor interface, sem muitas coisas na mesma tela.
        Instanciamos um objeto da telaHistorico para chamá-la.
        */
        TelaHistorico telaHistorico = new TelaHistorico(historico);
        
        /*
        Esse método que implementaremos, nos permitirá salvarmos o endereço da telaCalculadora e retornar à ela.
        */
        telaHistorico.pegarCalculadora(this);
        
        /*
        Não permitiremos que o usuário continue fazendo contas na calculadora enquanto vizualiza o histórico, pois a tabela de vizualização do histórico 
        só será atualizada ao abrir a janela histórico novamente, atravé do método construtor da janela.
        */
        this.setEnabled(false);
        telaHistorico.setVisible(true);

    }//GEN-LAST:event_jButtonHistoricoActionPerformed

